
public class Swamp {

	private String swampName;
	private String color;
	private String swampSize;

	public Swamp(String swampName, String color, String swampSize) {
		this.swampName = swampName;
		this.color = color;
		this.swampSize = swampSize;
	}

	public String getSwampName() {
		return swampName;
	}

	public void setSwampName(String swampName) {
		this.swampName = swampName;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getSwampSize() {
		return swampSize;
	}

	public void setSwampSize(String swampSize) {
		this.swampSize = swampSize;
	}

}
